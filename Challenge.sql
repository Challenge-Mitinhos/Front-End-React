drop table USUARIOS;

create table USUARIOS (
    ID_USU NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    EMAIL_USU VARCHAR(60) CONSTRAINT USU_EMAIL_NN NOT NULL
                          CONSTRAINT USU_EMAIL_UK UNIQUE,
    SENHA_USU VARCHAR(200) CONSTRAINT USU_SENHA_NN NOT NULL,
    NOME_USU VARCHAR(30) CONSTRAINT USU_NOME_NN NOT NULL,
    SOBRENOME_USU VARCHAR(50) CONSTRAINT USU_SOBRENM_NN NOT NULL,
    TEL_USU VARCHAR(15),
    CPF_USU VARCHAR(11) CONSTRAINT USU_CPF_NN NOT NULL
                        CONSTRAINT USU_CPF_UK UNIQUE);